function TextToSpeech
{
   Add-Type -AssemblyName System.speech
   $speak = New-Object System.Speech.Synthesis.SpeechSynthesizer
   $speak.SpeakAsync( $args ) | Out-Null
}

#==================================================================================================
# Aliases
#==================================================================================================

Set-Alias say TextToSpeech

